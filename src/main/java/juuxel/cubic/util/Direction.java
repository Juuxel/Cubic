package juuxel.cubic.util;

/**
 * This enum represents a direction on the screen.
 */
public enum Direction
{
    /**
     * This represents going left on the screen.
     */
    LEFT,

    /**
     * This represents going right on the screen.
     */
    RIGHT,

    /**
     * This represents going up on the screen.
     */
    UP,

    /**
     * This represents going down on the screen.
     */
    DOWN
}
